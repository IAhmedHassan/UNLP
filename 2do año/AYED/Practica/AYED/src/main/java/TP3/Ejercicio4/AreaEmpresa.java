package TP3.Ejercicio4;

public class AreaEmpresa {
    private String nombre;
    private int retardo;

    public AreaEmpresa(String unNombre, int unRetardo) {
        nombre = unNombre;
        retardo = unRetardo;
    }

    public String getNombre() {
        return this.nombre;
    }

    public int getRetardo() {
        return this.retardo;
    }
    
}
